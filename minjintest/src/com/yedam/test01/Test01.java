package com.yedam.test01;

public class Test01 {

	public static void main(String[] args) {
		
		int x = 10;
		double y = 2.0;
		
		double sum = x + y;
		double minus = x - y;
		double multiply = x * y;
		double division = x / y;
		
		System.out.println("x + y = " + (int)sum);
		System.out.println("x - y = " + (int)minus);
		System.out.println("x - y = " + (int)multiply);
		System.out.println("x - y = " + (int)division);
	}
	
}
